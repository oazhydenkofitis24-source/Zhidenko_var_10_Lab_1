import math

# Вихідні дані
R = 5   # радіус великої основи
r = 3   # радіус малої основи
H = 4   # висота конуса

# Крок 1: обчислення твірної
l = math.sqrt((R - r)**2 + H**2)

# Крок 2: обчислення площі поверхні
S = math.pi * R**2 + math.pi * r**2 + math.pi * (R + r) * l

# Крок 3: виведення результату з точністю до тисячних
print(f"Площа повної поверхні зрізаного конуса: {S:.3f}")