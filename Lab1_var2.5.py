# Введення довжин сторін трикутника
a = int(input("Введіть сторону a: "))
b = int(input("Введіть сторону b: "))
c = int(input("Введіть сторону c: "))

# Перевірка типу трикутника
if a == b == c:
    print(1)  # рівносторонній
elif a == b or b == c or a == c:
    print(2)  # рівнобедрений
else:
    print(3)  # різносторонній